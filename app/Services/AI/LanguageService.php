<?php

namespace App\Services\AI;

class LanguageService
{
    /*
    |--------------------------------------------------------------------------
    | DETECT LANGUAGE
    |--------------------------------------------------------------------------
    */

    public function detect(
        $text
    ) {

        /*
        |--------------------------------------------------------------------------
        | ARABIC
        |--------------------------------------------------------------------------
        */

        if (

            preg_match(
                '/[\x{0600}-\x{06FF}]/u',
                $text
            )

        ) {

            return 'ar';
        }

        /*
        |--------------------------------------------------------------------------
        | URDU/HINDI
        |--------------------------------------------------------------------------
        */

        if (

            preg_match(
                '/[\x{0900}-\x{097F}]/u',
                $text
            )

        ) {

            return 'hi';
        }

        /*
        |--------------------------------------------------------------------------
        | DEFAULT
        |--------------------------------------------------------------------------
        */

        return 'en';
    }

    /*
    |--------------------------------------------------------------------------
    | GET LANGUAGE NAME
    |--------------------------------------------------------------------------
    */

    public function name(
        $code
    ) {

        return [

            'en' => 'English',

            'ur' => 'Urdu',

            'ar' => 'Arabic',

            'hi' => 'Hindi',

            'bn' => 'Bengali'

        ][$code] ?? 'English';
    }
}